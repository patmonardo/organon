#[cfg(test)]
mod tests {
    use super::*;
    use crate::ml::nlp::sem::logic::parse;

    /// Count the number of literals in a CNF tree (helpful for shape
    /// assertions).
    fn count_clauses(e: &Expression) -> usize {
        match e {
            Expression::And(a, b) => count_clauses(a) + count_clauses(b),
            _ => 1,
        }
    }

    #[test]
    fn atom_is_unchanged() {
        let e = parse("man(adam)").unwrap();
        assert_eq!(skolemize(&e), e);
    }

    #[test]
    fn negated_atom_is_unchanged() {
        let e = parse("-man(adam)").unwrap();
        assert_eq!(skolemize(&e), e);
    }

    #[test]
    fn double_negation_collapses() {
        let e = parse("--man(adam)").unwrap();
        assert_eq!(skolemize(&e), parse("man(adam)").unwrap());
    }

    #[test]
    fn implication_becomes_disjunction() {
        // (man(adam) -> mortal(adam)) ⟹ (-man(adam) | mortal(adam))
        let e = parse("(man(adam) -> mortal(adam))").unwrap();
        let s = skolemize(&e);
        assert_eq!(s, parse("(-man(adam) | mortal(adam))").unwrap());
    }

    #[test]
    fn or_distributes_over_and() {
        // ((P & Q) | R) ⟹ ((P | R) & (Q | R))
        let e = parse("((man(a) & boy(a)) | dog(a))").unwrap();
        let s = skolemize(&e);
        assert_eq!(count_clauses(&s), 2);
    }

    #[test]
    fn existential_no_universal_uses_fresh_constant() {
        // some x. man(x)  ⟹  man(z2) (or some fresh z-var)
        let e = parse("some x. man(x)").unwrap();
        let s = skolemize(&e);
        // After skolemization the bound `x` is replaced; the result is
        // still an Application headed by `man`.
        match s {
            Expression::Application(f, arg) => {
                assert_eq!(*f, parse("man").unwrap());
                assert!(matches!(*arg, Expression::Variable(_)));
            }
            other => panic!("expected man(<sk>), got {:?}", other),
        }
    }

    #[test]
    fn existential_under_universal_uses_skolem_function() {
        reset_skolem_counter();
        // all x. some y. love(x, y)  ⟹  all x. love(x, F(x))
        // After universal removal: love(<fresh>, F(<fresh>))
        let e = parse("all x. some y. love(x, y)").unwrap();
        let s = skolemize(&e);
        // The result should be an application of love to two terms,
        // the second of which is itself an application (the skolem
        // function applied to the universal variable).
        match s {
            Expression::Application(love_x, sk_y) => match (*love_x, *sk_y) {
                (Expression::Application(_, _), Expression::Application(_, _)) => {}
                other => panic!("expected love(x, F(x)) shape, got {:?}", other),
            },
            other => panic!("expected love(...) application, got {:?}", other),
        }
    }

    #[test]
    fn negated_universal_becomes_existential() {
        // -all x. man(x)  ⟹  -man(<sk>)
        let e = parse("-all x. man(x)").unwrap();
        let s = skolemize(&e);
        match s {
            Expression::Negated(inner) => match *inner {
                Expression::Application(f, _) => {
                    assert_eq!(*f, parse("man").unwrap());
                }
                other => panic!("expected -man(<sk>), got {:?}", other),
            },
            other => panic!("expected negation, got {:?}", other),
        }
    }

    #[test]
    fn equality_is_unchanged() {
        let e = parse("(adam = adam)").unwrap();
        assert_eq!(skolemize(&e), e);
    }

    #[test]
    fn negated_equality_is_unchanged() {
        let e = parse("-(adam = betty)").unwrap();
        assert_eq!(skolemize(&e), e);
    }

    #[test]
    fn conjunction_recurses() {
        // (man(a) & some x. dog(x))  ⟹  (man(a) & dog(<sk>))
        let e = parse("(man(a) & some x. dog(x))").unwrap();
        let s = skolemize(&e);
        match s {
            Expression::And(a, b) => {
                assert_eq!(*a, parse("man(a)").unwrap());
                assert!(matches!(*b, Expression::Application(_, _)));
            }
            other => panic!("expected conjunction, got {:?}", other),
        }
    }
}
