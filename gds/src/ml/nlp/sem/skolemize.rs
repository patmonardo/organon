//! Skolemization + CNF conversion.
//!
//! Port of `nltk/sem/skolemize.py`. Removes existentials by replacing
//! their bound variables with Skolem functions of the enclosing
//! universal scope (or with Skolem constants when no universals are in
//! scope), and converts the result to **conjunctive normal form** by
//! pushing disjunction under conjunction.
//!
//! ## Algorithm
//!
//! Standard Skolemization:
//!
//! - `∀x. φ` → recurse with `x` added to the universal scope, then
//!   rename `x` to a fresh variable to keep variables apart.
//! - `∃x. φ` → recurse, then replace `x` with `F(u1,…,uk)` (the
//!   universal scope) or with a fresh Skolem constant if the scope is
//!   empty.
//! - `¬∀x. φ` ≡ `∃x. ¬φ` (and dually).
//! - Disjunction is pushed through conjunction by [`to_cnf`].
//! - Implication and biconditional are decomposed via the standard
//!   equivalences (`A → B ≡ ¬A ∨ B`; `A ↔ B ≡ (¬A ∨ B) ∧ (¬B ∨ A)`).
//!
//! Equality and atomic application expressions are left untouched.
//!
//! ## Skolem naming
//!
//! Skolem functions are named `F2`, `F3`, … — uppercase so they are
//! classified as **function variables** by NLTK convention
//! ([`Variable::is_function_name`]). Skolem constants (no universal
//! scope) are fresh **individual variables** generated by
//! [`unique_variable`], so they are still substitutable for equality
//! rewriting downstream (matching the choice already made in the
//! tableau prover).

use std::collections::BTreeSet;
use std::sync::atomic::{AtomicU64, Ordering};

use crate::ml::nlp::sem::logic::{unique_variable, Expression, Variable};

static SKOLEM_FN_COUNTER: AtomicU64 = AtomicU64::new(2);

/// Reset the skolem-function counter. Intended for deterministic tests.
pub fn reset_skolem_counter() {
    SKOLEM_FN_COUNTER.store(2, Ordering::SeqCst);
}

fn fresh_skolem_function(univ_scope: &BTreeSet<Variable>) -> Expression {
    let n = SKOLEM_FN_COUNTER.fetch_add(1, Ordering::SeqCst);
    let head = Expression::Variable(Variable::new(format!("F{}", n)));
    univ_scope.iter().fold(head, |acc, v| {
        Expression::app(acc, Expression::Variable(v.clone()))
    })
}

/// Skolemize `expression` and return its CNF form. Mirrors NLTK
/// `skolemize`.
pub fn skolemize(expression: &Expression) -> Expression {
    skolemize_inner(expression, &BTreeSet::new(), &BTreeSet::new())
}

fn skolemize_inner(
    expr: &Expression,
    univ_scope: &BTreeSet<Variable>,
    used_variables: &BTreeSet<Variable>,
) -> Expression {
    match expr {
        Expression::All(v, body) => {
            let mut next_scope = univ_scope.clone();
            next_scope.insert(v.clone());
            let mut next_used = used_variables.clone();
            next_used.insert(v.clone());
            let term = skolemize_inner(body, &next_scope, &next_used);
            let fresh = unique_variable(None, &next_used);
            term.replace(v, &Expression::Variable(fresh))
        }
        Expression::And(a, b) => Expression::and(
            skolemize_inner(a, univ_scope, used_variables),
            skolemize_inner(b, univ_scope, used_variables),
        ),
        Expression::Or(a, b) => to_cnf(
            &skolemize_inner(a, univ_scope, used_variables),
            &skolemize_inner(b, univ_scope, used_variables),
        ),
        Expression::Imp(a, b) => to_cnf(
            &skolemize_inner(&Expression::not((**a).clone()), univ_scope, used_variables),
            &skolemize_inner(b, univ_scope, used_variables),
        ),
        Expression::Iff(a, b) => Expression::and(
            to_cnf(
                &skolemize_inner(&Expression::not((**a).clone()), univ_scope, used_variables),
                &skolemize_inner(b, univ_scope, used_variables),
            ),
            to_cnf(
                &skolemize_inner(a, univ_scope, used_variables),
                &skolemize_inner(&Expression::not((**b).clone()), univ_scope, used_variables),
            ),
        ),
        Expression::Equality(_, _) => expr.clone(),
        Expression::Negated(inner) => skolemize_negated(inner, univ_scope, used_variables, expr),
        Expression::Exists(v, body) | Expression::Iota(v, body) => {
            let mut next_used = used_variables.clone();
            next_used.insert(v.clone());
            let term = skolemize_inner(body, univ_scope, &next_used);
            if !univ_scope.is_empty() {
                term.replace(v, &fresh_skolem_function(univ_scope))
            } else {
                let fresh = unique_variable(None, &next_used);
                term.replace(v, &Expression::Variable(fresh))
            }
        }
        Expression::Application(_, _)
        | Expression::Variable(_)
        | Expression::Constant(_)
        | Expression::Lambda(_, _) => expr.clone(),
    }
}

fn skolemize_negated(
    negated: &Expression,
    univ_scope: &BTreeSet<Variable>,
    used_variables: &BTreeSet<Variable>,
    original: &Expression,
) -> Expression {
    match negated {
        Expression::All(v, body) => {
            let mut next_used = used_variables.clone();
            next_used.insert(v.clone());
            let term = skolemize_inner(&Expression::not((**body).clone()), univ_scope, &next_used);
            if !univ_scope.is_empty() {
                term.replace(v, &fresh_skolem_function(univ_scope))
            } else {
                let fresh = unique_variable(None, &next_used);
                term.replace(v, &Expression::Variable(fresh))
            }
        }
        Expression::And(a, b) => to_cnf(
            &skolemize_inner(&Expression::not((**a).clone()), univ_scope, used_variables),
            &skolemize_inner(&Expression::not((**b).clone()), univ_scope, used_variables),
        ),
        Expression::Or(a, b) => Expression::and(
            skolemize_inner(&Expression::not((**a).clone()), univ_scope, used_variables),
            skolemize_inner(&Expression::not((**b).clone()), univ_scope, used_variables),
        ),
        Expression::Imp(a, b) => Expression::and(
            skolemize_inner(a, univ_scope, used_variables),
            skolemize_inner(&Expression::not((**b).clone()), univ_scope, used_variables),
        ),
        Expression::Iff(a, b) => Expression::and(
            to_cnf(
                &skolemize_inner(&Expression::not((**a).clone()), univ_scope, used_variables),
                &skolemize_inner(&Expression::not((**b).clone()), univ_scope, used_variables),
            ),
            to_cnf(
                &skolemize_inner(a, univ_scope, used_variables),
                &skolemize_inner(b, univ_scope, used_variables),
            ),
        ),
        Expression::Equality(_, _) => original.clone(),
        Expression::Negated(inner) => skolemize_inner(inner, univ_scope, used_variables),
        Expression::Exists(v, body) => {
            let mut next_scope = univ_scope.clone();
            next_scope.insert(v.clone());
            let mut next_used = used_variables.clone();
            next_used.insert(v.clone());
            let term = skolemize_inner(&Expression::not((**body).clone()), &next_scope, &next_used);
            let fresh = unique_variable(None, &next_used);
            term.replace(v, &Expression::Variable(fresh))
        }
        Expression::Application(_, _) | Expression::Variable(_) | Expression::Constant(_) => {
            original.clone()
        }
        // Iota / Lambda / nested negations not normally produced by
        // FOL surface; fall through to identity.
        _ => original.clone(),
    }
}

/// Distribute disjunction over conjunction. Mirrors NLTK
/// `to_cnf(first, second)`.
pub fn to_cnf(first: &Expression, second: &Expression) -> Expression {
    if let Expression::And(a, b) = first {
        Expression::and(to_cnf(a, second), to_cnf(b, second))
    } else if let Expression::And(a, b) = second {
        Expression::and(to_cnf(first, a), to_cnf(first, b))
    } else {
        Expression::or(first.clone(), second.clone())
    }
}

